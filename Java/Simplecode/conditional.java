package Java.Simplecode;

public class conditional{
public static void main(String args[]){
    int mark = 80;

    if (mark>=90){
        System.out.println("Grade A");
    }
    else if (mark>=70){
        System.out.println("grade C");
    }
    else if (mark>=60)
    {
        System.out.println("Grade D");
    }
    else{
        System.out.println("Failed");
    }
}
}